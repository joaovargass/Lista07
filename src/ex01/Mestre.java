package ex01;

public class Mestre extends Cavaleiro {
	private boolean imortal;
	private String campoDeVidencia;
	
	public boolean isImortal() {
		return imortal;
	}
	public void setImortal(boolean imortal) {
		this.imortal = imortal;
	}
	public String getCampoDeVidencia() {
		return campoDeVidencia;
	}
	public void setCampoDeVidencia(String campoDeVidencia) {
		this.campoDeVidencia = campoDeVidencia;
	}
	
}
